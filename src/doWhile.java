public class doWhile {
    public static void main(String[] args) {
        System.out.println("This chapter is based on do-While Loops in Java");

        int a = 1;
        do {
            System.out.println(a);
            a++;
        } while (a <= 100);

        // Quick quiz => Write a program to print 10 natural numbers in DoWhileLoop
        int b = 1;
        do {
            System.out.println(b);
            b++;
        } while (b <= 10);

    }
}
